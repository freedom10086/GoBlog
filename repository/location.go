// 获得所有的省
func GetProvinces() []Location {
	r := make([]Location, 0, len(locations))

	for _, v := range locations {
		v.Location = nil
		r = append(r, v)
	}

	return r
}

//获得所有的市
func GetCitys(province string) []Location {
	for _, v := range locations {
		if v.Name == province && v.Location != nil {
			r := make([]Location, 0, len(v.Location))
			for _, vv := range v.Location {
				vv.Location = nil
				r = append(r, vv)
			}
			return r
		}

	}
	return nil
}

//获得所有的市
func GetDistrics(province string, city string) []Location {
	for _, v := range locations {
		if v.Name == province && v.Location != nil {
			for _, vv := range v.Location {
				if vv.Name == city && vv.Location != nil {
					r := make([]Location, 0, len(v.Location))
					for _, vvv := range vv.Location {
						vvv.Location = nil
						r = append(r, vvv)
					}
					return r
				}
			}
		}
	}
	return nil
}
